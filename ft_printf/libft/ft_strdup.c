#include "libft.h"

char	*ft_strdup(const char *s)
{
	char			*dup;
	unsigned int	i;
	unsigned int	slen;

	slen = ft_strlen(s);
	dup = (char *)malloc(sizeof(char) * (slen + 1));
	if (!dup)
		return (NULL);
	i = 0;
	while (s[i])
	{
		dup[i] = s[i];
		i++;
	}
	dup[i] = '\0';
	return (dup);
}
